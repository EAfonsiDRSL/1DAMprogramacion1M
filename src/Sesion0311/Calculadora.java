package Sesion0311;

public class Calculadora {


    public int suma(int num1, int num2) {
        int resultado;
        resultado = num1+num2;
        return resultado;
    }
    public int resta(int num1, int num2){
        int resultado;
        resultado = num1-num2;
        return resultado;
    }
    public int multiplicacion(int num1, int num2){
        int resultado;
        resultado = num1*num2;
        return resultado;
    }
    public int division(int num1, int num2){
        int resultado;
        resultado = num1/num2;
        return resultado;
    }
}
