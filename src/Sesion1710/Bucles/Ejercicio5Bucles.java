package Sesion1710.Bucles;
/*
mostrar numero del 320 al 160 de 20 en 20 hacia atras
usar bucle 'while'
 */

public class Ejercicio5Bucles {
    public static void main(String[] args) {

        int numero =320;


        while (numero >=160){
            System.out.println(numero);
            numero -=20;
        }

    }
}
