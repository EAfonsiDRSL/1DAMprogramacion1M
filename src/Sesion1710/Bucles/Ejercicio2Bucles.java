package Sesion1710.Bucles;
/*
Mostrar multiplos de 5 de 0 a 100
usar 'while'
 */

public class Ejercicio2Bucles {
    public static void main(String[] args) {
        final int I=100;
        int numero=0;
        while (numero<=I){
            System.out.println(numero);
            numero +=5;
        }
    }
}
